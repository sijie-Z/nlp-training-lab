# 去重
seen = set()
unique_pairs = []
for q, a in PAIRS:
    if q not in seen:
        seen.add(q)
        unique_pairs.append((q, a))

random.shuffle(unique_pairs)

# 拆分 40 训练 + 10 验证
train = unique_pairs[:40]
val = unique_pairs[40:50]

os.makedirs("data/splits", exist_ok=True)

def write_jsonl(pairs, path):
    with open(path, "w", encoding="utf-8") as f:
        for q, a in pairs:
            f.write(json.dumps({"instruction": q, "output": a}, ensure_ascii=False) + "\n")

write_jsonl(train, "data/splits/lora_train.jsonl")
write_jsonl(val, "data/splits/lora_val.jsonl")

print(f"Train: {len(train)} pairs")
print(f"Val:   {len(val)} pairs")
for q, a in train[:5]:
    print(f"  [{q}] → [{a[:30]}...]")
