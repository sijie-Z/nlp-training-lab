# 最终数据
all_data = []
seen = set()

def add_pair(text_a, text_b, label):
    key = (text_a, text_b)
    if key not in seen:
        seen.add(key)
        all_data.append((text_a, text_b, label))

for a, b in positive_pairs:
    add_pair(a, b, 1)
for a, b in negative_pairs:
    add_pair(a, b, 0)
for a, b in hard_negatives:
    add_pair(a, b, 0)

random.shuffle(all_data)

# 输出
os.makedirs("data/splits", exist_ok=True)

# 拆分训练/验证/测试
n = len(all_data)
train_end = int(n * 0.7)
val_end = int(n * 0.85)

train_data = all_data[:train_end]
val_data = all_data[train_end:val_end]
test_data = all_data[val_end:]

def write_tsv(data, path):
    with open(path, "w", encoding="utf-8") as f:
        for a, b, label in data:
            f.write(f"{a}\t{b}\t{label}\n")
    print(f"  {path}: {len(data)} pairs")

print("Generating LCQMC-style dataset:")
write_tsv(train_data, "data/splits/match_train.tsv")
write_tsv(val_data, "data/splits/match_val.tsv")
write_tsv(test_data, "data/splits/match_test.tsv")

pos = sum(1 for _, _, l in train_data if l == 1)
neg = len(train_data) - pos
print(f"\nTrain: {len(train_data)} pairs ({pos} positive, {neg} negative)")
print(f"Val:   {len(val_data)} pairs")
print(f"Test:  {len(test_data)} pairs")

